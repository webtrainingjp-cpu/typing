<?php
// =====================================================
// Maintenance Script（古いランキング削除）
// ・7日以上前のスコアを削除する
// ・手動実行 or 定期実行（cron想定）
// =====================================================

// 厳密な型チェック（予期しない型ミス防止）
declare(strict_types=1);


// =====================================================
// ① セキュリティ（簡易認証）
// =====================================================
// NOTE:
// URL直叩き用の簡易キー認証
// 本番では必ず推測困難な文字列に変更すること

// $secretKey = 'secret123';
$secretKey = 'typing2026'; // 本番用例

$inputKey = isset($_GET['key']) ? (string) $_GET['key'] : '';

// キー不一致 → 即終了（DB処理させない）
if ($inputKey !== $secretKey) {
    echo 'アクセス拒否';
    exit;
}


try {
    // =====================================================
    // ② DB接続
    // =====================================================
    $config = require __DIR__ . '/config.php';
    $db = $config['db'] ?? [];

    $pdo = new PDO(
        $db['dsn'] ?? '',
        $db['user'] ?? '',
        $db['pass'] ?? '',
        [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
            PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC,
        ]
    );


    // =====================================================
    // ③ 古いデータ削除
    // =====================================================
    // NOTE:
    // created_at を基準に「7日より前」を削除
    // weeklyランキングのデータ肥大化を防ぐ目的

    $stmt = $pdo->prepare(
        'DELETE FROM scores
         WHERE created_at < DATE_SUB(NOW(), INTERVAL 7 DAY)'
    );

    $stmt->execute();


    // =====================================================
    // ④ 実行結果出力
    // =====================================================
    // rowCount() → 削除された件数

    echo $stmt->rowCount() . '件の古いデータを削除しました';
} catch (Throwable $e) {

    // =====================================================
    // エラー時出力（簡易ログ）
    // =====================================================
    echo '削除に失敗しました: ' . $e->getMessage();
}


// =====================================================
// 実行方法メモ（開発用）
// =====================================================
// NOTE:
// ブラウザ or curl で直接実行
// 本番ではURL公開に注意（キー漏洩リスクあり）

// 開発環境
// http://localhost/typing/api/delete-old-scores.php?key=secret123

